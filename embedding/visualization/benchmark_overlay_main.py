from __future__ import annotations

import argparse
import json
import os
import sys
from dataclasses import dataclass
from pathlib import Path
from typing import Any, Dict, List, Mapping, Optional, Sequence

# Ensure project-root imports work when this file is executed directly.
CURRENT_DIR = os.path.dirname(os.path.abspath(__file__))
PROJECT_ROOT = os.path.abspath(os.path.join(CURRENT_DIR, "..", ".."))

if PROJECT_ROOT not in sys.path:
    sys.path.insert(0, PROJECT_ROOT)

from Chunking.config.settings import PipelineSettings
from embedding.models import EmbeddingVectorRecord
from embedding.provider_factory import EmbeddingProvider, create_embedding_provider
from embedding.visualization.benchmark_overlay_export import (
    BenchmarkOverlayExportResult,
    export_benchmark_overlay_dataset,
)
from retrieval.evaluation.benchmark_loader import BenchmarkLoader


@dataclass(slots=True)
class BenchmarkOverlayCliResult:
    """
    Describe one benchmark overlay CLI execution.

    Attributes
    ----------
    chunk_vectors_path : Path
        Source Spotlight JSONL file containing exported chunk vectors.

    export_result : BenchmarkOverlayExportResult
        Summary returned by the overlay exporter.
    """

    chunk_vectors_path: Path
    export_result: BenchmarkOverlayExportResult


def run_benchmark_overlay_main(
    *,
    settings: Optional[PipelineSettings] = None,
    questions_path: Optional[Path] = None,
    chunk_vectors_path: Optional[Path] = None,
    output_path: Optional[Path] = None,
    strategy_name: Optional[str] = None,
    run_id: Optional[str] = None,
    provider: Optional[EmbeddingProvider] = None,
) -> BenchmarkOverlayCliResult:
    """
    Generate the benchmark-question vs chunk embedding overlay dataset.

    Parameters
    ----------
    settings : Optional[PipelineSettings]
        Shared project settings. Default settings are loaded when omitted.

    questions_path : Optional[Path]
        Optional benchmark questions JSONL path override.

    chunk_vectors_path : Optional[Path]
        Optional Spotlight JSONL file containing existing chunk embeddings.

    output_path : Optional[Path]
        Optional overlay JSONL destination.

    strategy_name : Optional[str]
        Optional embedding strategy folder used when resolving chunk vectors.

    run_id : Optional[str]
        Optional embedding run folder used when resolving chunk vectors.

    provider : Optional[EmbeddingProvider]
        Optional embedding provider injection for deterministic tests.

    Returns
    -------
    BenchmarkOverlayCliResult
        Summary of the generated overlay artifact.
    """

    resolved_settings = settings or PipelineSettings()
    resolved_chunk_vectors_path = _resolve_chunk_vectors_path(
        chunk_vectors_path=chunk_vectors_path,
        output_root=resolved_settings.embedding_output_root,
        strategy_name=strategy_name,
        run_id=run_id,
    )
    resolved_output_path = Path(
        output_path
        or resolved_settings.embedding_visualization_benchmark_overlay_output_path
    )

    benchmark_cases = BenchmarkLoader(
        settings=resolved_settings,
        questions_path=questions_path,
    ).load_question_cases()
    chunk_records = load_chunk_embedding_records(resolved_chunk_vectors_path)
    question_embeddings = _generate_question_embeddings(
        benchmark_cases=benchmark_cases,
        provider=provider or create_embedding_provider(resolved_settings),
    )

    export_result = export_benchmark_overlay_dataset(
        chunk_embedding_records=chunk_records,
        benchmark_question_cases=benchmark_cases,
        benchmark_question_embeddings=question_embeddings,
        output_path=resolved_output_path,
    )
    return BenchmarkOverlayCliResult(
        chunk_vectors_path=resolved_chunk_vectors_path,
        export_result=export_result,
    )


def load_chunk_embedding_records(chunk_vectors_path: Path) -> List[EmbeddingVectorRecord]:
    """
    Load chunk embedding records from a Spotlight JSONL export.

    Parameters
    ----------
    chunk_vectors_path : Path
        Existing `spotlight_dataset.jsonl` file generated by the embedding flow.

    Returns
    -------
    List[EmbeddingVectorRecord]
        Ordered chunk embedding records suitable for overlay export.
    """

    validated_path = _validate_chunk_vectors_path(Path(chunk_vectors_path))
    records: List[EmbeddingVectorRecord] = []

    try:
        with validated_path.open("r", encoding="utf-8") as vectors_file:
            for line_number, raw_line in enumerate(vectors_file, start=1):
                stripped_line = raw_line.strip()
                if not stripped_line:
                    continue
                records.append(
                    _build_chunk_record(
                        payload=_parse_jsonl_object(
                            raw_line=stripped_line,
                            input_path=validated_path,
                            line_number=line_number,
                        ),
                        input_path=validated_path,
                        line_number=line_number,
                    )
                )
    except OSError as exc:
        raise ValueError(
            f"Cannot read chunk vectors from '{validated_path}': {exc}"
        ) from exc

    if not records:
        raise ValueError(f"Chunk vectors file '{validated_path}' contains no records.")

    return records


def _generate_question_embeddings(
    *,
    benchmark_cases: Sequence[Any],
    provider: EmbeddingProvider,
) -> Dict[str, List[float]]:
    """
    Generate benchmark-question embeddings keyed by benchmark case id.
    """

    question_texts = [benchmark_case.question for benchmark_case in benchmark_cases]
    vectors = provider.embed_texts(question_texts)

    if len(vectors) != len(benchmark_cases):
        raise ValueError(
            "Embedding provider returned a vector count that does not match "
            "the benchmark question count."
        )

    return {
        benchmark_case.case_id: list(vector)
        for benchmark_case, vector in zip(benchmark_cases, vectors)
    }


def _resolve_chunk_vectors_path(
    *,
    chunk_vectors_path: Optional[Path],
    output_root: Path,
    strategy_name: Optional[str],
    run_id: Optional[str],
) -> Path:
    """
    Resolve the chunk vector export path from explicit input or run folders.
    """

    if chunk_vectors_path is not None:
        return _validate_chunk_vectors_path(Path(chunk_vectors_path).expanduser())

    return _resolve_latest_spotlight_dataset(
        output_root=Path(output_root),
        strategy_name=strategy_name,
        run_id=run_id,
    )


def _resolve_latest_spotlight_dataset(
    *,
    output_root: Path,
    strategy_name: Optional[str],
    run_id: Optional[str],
) -> Path:
    """
    Resolve the newest Spotlight chunk-vector export under the embedding output root.
    """

    if not output_root.exists():
        raise FileNotFoundError(
            "Embedding output root does not exist. Run the embedding pipeline "
            "with Spotlight export enabled before generating the overlay."
        )

    if run_id and not strategy_name:
        raise ValueError("A strategy name is required when a run id is provided.")

    search_root = output_root
    if strategy_name:
        normalized_strategy_name = strategy_name.strip()
        if not normalized_strategy_name:
            raise ValueError("Strategy name cannot be empty.")
        search_root = search_root / normalized_strategy_name

    if run_id:
        normalized_run_id = run_id.strip()
        if not normalized_run_id:
            raise ValueError("Run id cannot be empty.")
        search_root = search_root / normalized_run_id

    if not search_root.exists():
        raise FileNotFoundError(f"No Spotlight exports were found under '{search_root}'.")

    candidate_paths = sorted(
        search_root.rglob("spotlight_dataset.jsonl"),
        key=lambda path: path.stat().st_mtime,
        reverse=True,
    )
    if not candidate_paths:
        raise FileNotFoundError(
            f"No Spotlight dataset was found under '{search_root}'."
        )

    return _validate_chunk_vectors_path(candidate_paths[0])


def _validate_chunk_vectors_path(chunk_vectors_path: Path) -> Path:
    """
    Validate one Spotlight chunk-vector export path.
    """

    resolved_path = chunk_vectors_path.expanduser().resolve()
    if not resolved_path.exists():
        raise FileNotFoundError(f"Chunk vectors file '{resolved_path}' does not exist.")
    if not resolved_path.is_file():
        raise ValueError(f"Chunk vectors path '{resolved_path}' is not a file.")
    if resolved_path.name != "spotlight_dataset.jsonl":
        raise ValueError(
            "Benchmark overlay expects a 'spotlight_dataset.jsonl' chunk-vector export."
        )
    return resolved_path


def _parse_jsonl_object(
    *,
    raw_line: str,
    input_path: Path,
    line_number: int,
) -> Dict[str, Any]:
    """
    Parse one JSONL object with deterministic file and line context.
    """

    try:
        payload = json.loads(raw_line)
    except json.JSONDecodeError as exc:
        raise ValueError(
            f"Invalid JSON in '{input_path}' at line {line_number}: {exc}"
        ) from exc

    if not isinstance(payload, dict):
        raise ValueError(
            f"Invalid JSONL row in '{input_path}' at line {line_number}: "
            "line must be an object."
        )
    return payload


def _build_chunk_record(
    *,
    payload: Mapping[str, Any],
    input_path: Path,
    line_number: int,
) -> EmbeddingVectorRecord:
    """
    Convert one Spotlight row into an embedding vector record.
    """

    chunk_id = _read_required_string(payload, "chunk_id", input_path, line_number)
    doc_id = _read_required_string(payload, "doc_id", input_path, line_number)
    vector = _read_numeric_vector(payload, "embedding", input_path, line_number)
    metadata = _build_chunk_metadata(payload)

    return EmbeddingVectorRecord(
        chunk_id=chunk_id,
        doc_id=doc_id,
        vector=vector,
        metadata=metadata,
        model=_read_string(payload.get("model")),
        provider=_read_string(payload.get("provider")),
        source_file=_read_string(payload.get("source_file")),
        text=_read_string(payload.get("text")),
        record_id=_read_string(payload.get("record_id")) or chunk_id,
    )


def _build_chunk_metadata(payload: Mapping[str, Any]) -> Dict[str, Any]:
    """
    Build metadata needed by the overlay exporter from a Spotlight row.
    """

    metadata: Dict[str, Any] = {}

    for key, value in payload.items():
        if isinstance(key, str) and key.startswith("metadata_"):
            metadata[key.removeprefix("metadata_")] = value

    for field_name in (
        "document_title",
        "article_number",
        "article_title",
        "hierarchy_path",
        "page_start",
        "page_end",
        "strategy",
    ):
        if field_name in payload and payload[field_name] is not None:
            metadata[field_name] = payload[field_name]

    return metadata


def _read_required_string(
    payload: Mapping[str, Any],
    field_name: str,
    input_path: Path,
    line_number: int,
) -> str:
    """
    Read one required non-empty string field from a Spotlight row.
    """

    value = _read_string(payload.get(field_name))
    if value:
        return value
    raise ValueError(
        f"Invalid chunk vectors row in '{input_path}' at line {line_number}: "
        f"field '{field_name}' must be a non-empty string."
    )


def _read_numeric_vector(
    payload: Mapping[str, Any],
    field_name: str,
    input_path: Path,
    line_number: int,
) -> List[float]:
    """
    Read one required numeric vector field from a Spotlight row.
    """

    value = payload.get(field_name)
    if not isinstance(value, list):
        raise ValueError(
            f"Invalid chunk vectors row in '{input_path}' at line {line_number}: "
            f"field '{field_name}' must be a numeric list."
        )

    vector: List[float] = []
    for item in value:
        if isinstance(item, bool) or not isinstance(item, (int, float)):
            raise ValueError(
                f"Invalid chunk vectors row in '{input_path}' at line "
                f"{line_number}: field '{field_name}' must contain only numbers."
            )
        vector.append(float(item))

    if not vector:
        raise ValueError(
            f"Invalid chunk vectors row in '{input_path}' at line {line_number}: "
            f"field '{field_name}' cannot be empty."
        )
    return vector


def _read_string(value: Any) -> str:
    """
    Normalize optional string input into a stripped string.
    """

    if isinstance(value, str):
        return value.strip()
    return ""


def _build_argument_parser() -> argparse.ArgumentParser:
    """
    Build the command-line parser for the benchmark overlay generator.
    """

    parser = argparse.ArgumentParser(
        description="Generate a chunk-vs-benchmark-question embedding overlay dataset."
    )
    parser.add_argument(
        "--questions-path",
        type=Path,
        help="Optional benchmark questions JSONL path.",
    )
    parser.add_argument(
        "--chunk-vectors-path",
        type=Path,
        help="Optional Spotlight chunk-vector JSONL export.",
    )
    parser.add_argument(
        "--output-path",
        type=Path,
        help="Optional overlay JSONL output path.",
    )
    parser.add_argument(
        "--strategy",
        dest="strategy_name",
        help="Optional embedding strategy folder for automatic chunk-vector lookup.",
    )
    parser.add_argument(
        "--run-id",
        help="Optional embedding run folder for automatic chunk-vector lookup.",
    )
    return parser


def main(argv: Optional[Sequence[str]] = None) -> int:
    """
    Run the benchmark overlay CLI and print the generated artifact location.
    """

    parser = _build_argument_parser()
    arguments = parser.parse_args(argv)

    try:
        result = run_benchmark_overlay_main(
            settings=PipelineSettings(),
            questions_path=arguments.questions_path,
            chunk_vectors_path=arguments.chunk_vectors_path,
            output_path=arguments.output_path,
            strategy_name=arguments.strategy_name,
            run_id=arguments.run_id,
        )
    except (FileNotFoundError, RuntimeError, ValueError) as exc:
        print(f"[ERROR] {exc}", file=sys.stderr)
        return 1

    export_result = result.export_result
    print(f"[INFO] Chunk vectors path: {result.chunk_vectors_path}")
    print(f"[INFO] Overlay output path: {export_result.output_path}")
    print(f"[INFO] Chunk points: {export_result.chunk_point_count}")
    print(
        "[INFO] Benchmark question points: "
        f"{export_result.benchmark_question_point_count}"
    )
    print(f"[INFO] Embedding dimension: {export_result.embedding_dimension}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
